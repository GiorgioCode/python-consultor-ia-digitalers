print(f"Resta: {primer_numero - segundo_numero}")
print(f"Multiplicacion: {primer_numero * segundo_numero}")
print(f"Division: {primer_numero / segundo_numero}")
print(f"El primer numero es mayor: {primer_numero > segundo_numero}")

